<template>
    <div class="container">
        <h1>Editer un niveau d'Ã©tude</h1>
        <app-form :values="form" v-on:submit.prevent="onSubmit" v-if="!isLoading">
            <app-form-field name="label">
                <app-form-label>Nom du niveau</app-form-label>
                <app-form-input></app-form-input>
            </app-form-field>
            <app-form-button>Modifier</app-form-button>
        </app-form>
    </div>
</template>

<script>
import degreesGateway from '../../services/gateway/degrees.gateway';
export default {
    props: ['id'],
    data() {
        return {
            form: {
                label: ''
            },
            isLoading: true
        }
    },
    created() {
        degreesGateway.getDegree(this.id)
        .then(degree => {
            this.form.label = degree.label;
            this.isLoading = false;
        })
    },
    methods: {
        onSubmit() {
            const newDegree = {...this.form};
            degreesGateway.editDegree(this.id, newDegree);
        }
    }
    
}
</script>